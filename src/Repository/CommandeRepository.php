   public function findByCommande(): array
   {
       return $this->createQueryBuilder('c')
       ->select('c', 'd')
       ->innerJoin(User::class, 'a', 'c.user=a.id')
       ->innerJoin(CommandeDetail::class, 'd', 'c.id=d.commande')
       ->Where('c.isPaid =true ')
        ->getQuery()
        ->getResult()
